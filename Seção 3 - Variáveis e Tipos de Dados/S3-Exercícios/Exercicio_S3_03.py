"""
Faça um programa que recebe três valores e apresente a soma dos quadrados dos valores lidos
"""

valor1 = int(input('Informe o primeiro valor: '))
valor2 = int(input('Informe o segundo valor: '))
valor3 = int(input('Informe o terceiro valor: '))
soma = ((valor1**2) + (valor2**2) + (valor3**2))

print(f'A soma dos quadrados dos valores {valor1}, {valor2}, {valor3} é {soma}.')